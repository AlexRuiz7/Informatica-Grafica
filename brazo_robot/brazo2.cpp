#include "brazo2.h"

//----------------------------------------------------------------------------//

  Brazo2::Brazo2(){}

//----------------------------------------------------------------------------//

  void Brazo2::draw(short mode){
    glPushMatrix();
    glTranslatef(0,10,0);
    glScalef(5,20,5);
    cubo.draw(mode);
    glPopMatrix();

    glPushMatrix();
    glTranslatef(0,20,0);
    glRotatef(a,0,0,1);
    brazo1.draw(mode);
    glPopMatrix();
  }

//----------------------------------------------------------------------------//

  void Brazo2::setBrazo1(float x){
    a = x;
  }

  //----------------------------------------------------------------------------//

  void Brazo2::setPinza(float r){
    brazo1.setPinza(r);
  }
